// MARK: - Список статей

final class ArticlesListViewController: UIViewController {

    private let tableView: UITableView = {
        let tv = UITableView(frame: .zero, style: .insetGrouped)
        tv.backgroundColor = .systemGroupedBackground
        tv.translatesAutoresizingMaskIntoConstraints = false
        return tv
    }()

    override func viewDidLoad() {
        super.viewDidLoad()
        title = "Статьи"
        view.backgroundColor = .systemGroupedBackground
        navigationItem.largeTitleDisplayMode = .never

        view.addSubview(tableView)
        NSLayoutConstraint.activate([
            tableView.topAnchor.constraint(equalTo: view.topAnchor),
            tableView.leadingAnchor.constraint(equalTo: view.leadingAnchor),
            tableView.trailingAnchor.constraint(equalTo: view.trailingAnchor),
            tableView.bottomAnchor.constraint(equalTo: view.bottomAnchor)
        ])
        tableView.dataSource = self
        tableView.delegate = self
        tableView.register(UITableViewCell.self, forCellReuseIdentifier: "article")
    }

    override func viewWillAppear(_ animated: Bool) {
        super.viewWillAppear(animated)
        // Вернулись из читалки — статусы могли измениться
        tableView.reloadData()
    }
}

extension ArticlesListViewController: UITableViewDataSource, UITableViewDelegate {

    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        ArticleCatalog.all.count
    }

    func tableView(_ tableView: UITableView, titleForHeaderInSection section: Int) -> String? {
        "Прочитано \(ArticleReadStore.readCount) из \(ArticleCatalog.all.count)"
    }

    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let article = ArticleCatalog.all[indexPath.row]
        let cell = tableView.dequeueReusableCell(withIdentifier: "article", for: indexPath)
        var config = UIListContentConfiguration.subtitleCell()
        config.text = "\(article.emoji) \(article.title)"
        config.textProperties.font = .systemFont(ofSize: 15, weight: .medium)
        config.secondaryText = "\(article.subtitle) · \(article.readMinutes) мин"
        config.secondaryTextProperties.color = .secondaryLabel
        config.secondaryTextProperties.font = .systemFont(ofSize: 13)

        // Статус прочтения: зелёная галка и приглушённый заголовок
        let isRead = ArticleReadStore.isRead(article.id)
        config.image = UIImage(systemName: isRead ? "checkmark.circle.fill" : "circle")
        config.imageProperties.tintColor = isRead ? .systemGreen : .tertiaryLabel
        config.imageProperties.maximumSize = CGSize(width: 22, height: 22)
        config.textProperties.color = isRead ? .secondaryLabel : .label

        cell.contentConfiguration = config
        cell.accessoryType = .disclosureIndicator
        return cell
    }

    // Свайп: пометить/снять пометку, не открывая статью
    func tableView(_ tableView: UITableView,
                   leadingSwipeActionsConfigurationForRowAt indexPath: IndexPath)
        -> UISwipeActionsConfiguration? {
        let article = ArticleCatalog.all[indexPath.row]
        let isRead = ArticleReadStore.isRead(article.id)
        let action = UIContextualAction(
            style: .normal,
            title: isRead ? "Не прочитано" : "Прочитано"
        ) { _, _, done in
            ArticleReadStore.toggle(article.id)
            tableView.reloadRows(at: [indexPath], with: .automatic)
            done(true)
        }
        action.backgroundColor = isRead ? .systemGray : .systemGreen
        action.image = UIImage(systemName: isRead ? "circle" : "checkmark.circle.fill")
        return UISwipeActionsConfiguration(actions: [action])
    }

    func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath) {
        tableView.deselectRow(at: indexPath, animated: true)
        let reader = ArticleViewController(article: ArticleCatalog.all[indexPath.row])
        navigationController?.pushViewController(reader, animated: true)
    }
}

// MARK: - Читалка

final class ArticleViewController: UIViewController {

    private let article: Article

    init(article: Article) {
        self.article = article
        super.init(nibName: nil, bundle: nil)
    }

    required init?(coder: NSCoder) {
        fatalError("init(coder:) has not been implemented")
    }

    override func viewDidLoad() {
        super.viewDidLoad()
        view.backgroundColor = .systemBackground
        navigationItem.largeTitleDisplayMode = .never

        let scrollView = UIScrollView()
        scrollView.translatesAutoresizingMaskIntoConstraints = false

        let emojiLabel = UILabel()
        emojiLabel.text = article.emoji
        emojiLabel.font = .systemFont(ofSize: 52)

        let titleLabel = UILabel()
        titleLabel.text = article.title
        titleLabel.font = DQFont.rounded(26, weight: .bold)
        titleLabel.numberOfLines = 0

        let metaLabel = UILabel()
        metaLabel.text = "\(article.subtitle) · \(article.readMinutes) мин чтения"
        metaLabel.font = .systemFont(ofSize: 13)
        metaLabel.textColor = .secondaryLabel
        metaLabel.numberOfLines = 0

        let bodyLabel = UILabel()
        bodyLabel.numberOfLines = 0
        // Атрибуты ради межстрочного интервала — голый UILabel
        // с длинным текстом читается тяжело.
        let paragraph = NSMutableParagraphStyle()
        paragraph.lineSpacing = 5
        paragraph.paragraphSpacing = 12
        bodyLabel.attributedText = NSAttributedString(
            string: article.body,
            attributes: [
                .font: UIFont.systemFont(ofSize: 16),
                .foregroundColor: UIColor.label,
                .paragraphStyle: paragraph
            ]
        )

        var readConfig = UIButton.Configuration.filled()
        readConfig.cornerStyle = .large
        readConfig.buttonSize = .large
        let readButton = UIButton(configuration: readConfig)
        readButton.addTarget(self, action: #selector(toggleRead), for: .touchUpInside)
        self.readButton = readButton

        let stack = UIStackView(arrangedSubviews: [emojiLabel, titleLabel, metaLabel, bodyLabel, readButton])
        stack.axis = .vertical
        stack.spacing = 8
        stack.setCustomSpacing(16, after: metaLabel)
        stack.setCustomSpacing(28, after: bodyLabel)
        stack.translatesAutoresizingMaskIntoConstraints = false

        view.addSubview(scrollView)
        scrollView.addSubview(stack)

        NSLayoutConstraint.activate([
            scrollView.topAnchor.constraint(equalTo: view.topAnchor),
            scrollView.leadingAnchor.constraint(equalTo: view.leadingAnchor),
            scrollView.trailingAnchor.constraint(equalTo: view.trailingAnchor),
            scrollView.bottomAnchor.constraint(equalTo: view.bottomAnchor),

            stack.topAnchor.constraint(equalTo: scrollView.contentLayoutGuide.topAnchor, constant: 20),
            stack.leadingAnchor.constraint(equalTo: scrollView.contentLayoutGuide.leadingAnchor, constant: 20),
            stack.trailingAnchor.constraint(equalTo: scrollView.contentLayoutGuide.trailingAnchor, constant: -20),
            stack.bottomAnchor.constraint(equalTo: scrollView.contentLayoutGuide.bottomAnchor, constant: -32),
            stack.widthAnchor.constraint(equalTo: scrollView.frameLayoutGuide.widthAnchor, constant: -40)
        ])

        refreshReadButton()
    }

    private weak var readButton: UIButton?

    private func refreshReadButton() {
        guard let button = readButton else { return }
        let isRead = ArticleReadStore.isRead(article.id)
        var config = button.configuration
        config?.title = isRead ? "Прочитано ✓" : "Отметить прочитанной · +\(AppConfig.articleReadXP) XP"
        config?.baseBackgroundColor = isRead ? .systemGreen.withAlphaComponent(0.18) : .systemIndigo
        config?.baseForegroundColor = isRead ? .systemGreen : .white
        button.configuration = config
    }

    @objc private func toggleRead() {
        ArticleReadStore.toggle(article.id)
        Haptics.success()
        refreshReadButton()
    }
}

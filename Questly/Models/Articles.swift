enum ArticleReadStore {

    private static let key = "dq_articles_read"

    private static var readIDs: Set<String> {
        get { Set(UserDefaults.standard.stringArray(forKey: key) ?? []) }
        set { UserDefaults.standard.set(Array(newValue), forKey: key) }
    }

    static func isRead(_ id: String) -> Bool {
        readIDs.contains(id)
    }

    static func toggle(_ id: String) {
        var ids = readIDs
        if ids.contains(id) {
            ids.remove(id)
        } else {
            ids.insert(id)
            awardXPIfFirstTime(id)
        }
        readIDs = ids
    }

    // XP за статью — один раз за всю жизнь отметки. Снял и поставил
    // заново — повторного начисления нет, иначе это станок для фарма.
    private static let xpKey = "dq_articles_xp_awarded"

    private static func awardXPIfFirstTime(_ id: String) {
        var awarded = Set(UserDefaults.standard.stringArray(forKey: xpKey) ?? [])
        guard awarded.insert(id).inserted else { return }
        UserDefaults.standard.set(Array(awarded), forKey: xpKey)
        DataStore.shared.awardBonusXP(AppConfig.articleReadXP)
    }

    /// Полный сброс: прочитанное и история начисления XP.
    /// Вызывается из deleteAllData() при полном сбросе приложения.
    static func reset() {
        UserDefaults.standard.removeObject(forKey: key)
        UserDefaults.standard.removeObject(forKey: xpKey)
    }

    static var readCount: Int {
        // Считаем только существующие статьи (id из старых версий не мешают)
        ArticleCatalog.all.filter { readIDs.contains($0.id) }.count
    }
}

